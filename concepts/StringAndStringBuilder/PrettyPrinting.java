package concepts.StringAndStringBuilder;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 12.4582f;

        // use printf to printf formatted strings
        System.out.printf("Formatted number is %.2f %n", a); // round off value
        System.out.println(Math.PI);
        System.out.printf("%.3f", Math.PI);

    }
}
